WITH w1 AS (SELECT '1,2,3' AS c1 FROM DUAL)
SELECT REGEXP_SUBSTR (a.c1, '[^,]+', 1, b.lv) AS c1
  FROM w1 a
     , (SELECT LEVEL AS lv FROM DUAL CONNECT BY LEVEL <= 10) b
 WHERE b.lv <= REGEXP_COUNT (a.c1, ',') + 1;
